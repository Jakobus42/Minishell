#include "libft.h"

int	ft_isalnum(int nc)
{
	if (((nc >= 'A' && nc <= 'Z') || (nc >= 'a' && nc <= 'z')
			|| (nc >= '0' && nc <= '9')))
		return (8);
	return (0);
}
